    private void genButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_genButtonActionPerformed
        this.outTextArea.setText(null);

        int length, amount;
        String name, ID;

        length = this.lengthOption.getSelectedIndex() + 6;
        amount = Integer.parseInt(this.nKey.getText());
        name = this.nameInputField.getText();
        ID = this.id_InputField.getText();

        NameandID ni = new NameandID(length, amount, name, ID);

        String[] keyForPrintOut = new String[amount];
        keyForWriteOut = keyForPrintOut = ni.compilePassForGUI();

        int i = 0;
        while (i < keyForPrintOut.length) {
            this.outTextArea.insert(keyForPrintOut[i] + "\n", 0);
            i++;
        }
    }//GEN-LAST:event_genButtonActionPerformed

